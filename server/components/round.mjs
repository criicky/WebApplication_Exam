export default function Round(round, game, username, meme, score) {
    
    this.round = round;        //round inside the game
    this.game = game;         //id of the game
    this.username = username;   //username of the player
    this.meme = meme;       //id of the meme
    this.score = score;     //5 or 0 nothing else
    
}